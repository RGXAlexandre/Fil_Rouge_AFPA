<h2>RTFM (expression)</h2>
<br>
RTFM est, en anglais, le sigle de la phrase Read the fucking manual (littéralement « lis le putain de manuel »). Cette instruction est parfois donnée quand la réponse à une question peut être facilement trouvée dans un mode d'emploi. Elle est surtout utilisée dans le jargon informatique, par exemple dans les forums de discussion.
<br>
<br>

Cette expression a été créée avant l'avènement d'Internet et de Google, alors que les modes d'emploi étaient sur papier, ou accessibles sur les systèmes POSIX à l'aide de la commande man. Dans ce dernier cas, RTFM voulait alors dire Read The Fucking Man page. Depuis, les modes d'emploi sont en ligne, ce qui a entrainé la création de sigles et d'expressions similaires comme :
<br>
<br>

    RTMF (Read The Manual First, lis le manuel en premier), version plus polie de cette expression ;
    <br>
    <br>
    GIYF (Google is your friend, Google est ton ami) ;
    <br>
    <br>
    GETA (Google est ton ami, version française) ;
    <br>
    <br>
    LMGTFY (Let me google that for you, laisse moi chercher dans google pour toi).
    <br>
    <br>

Toutes ces nouvelles expressions indiquent que la réponse à une question peut être facilement trouvée sur Internet via une recherche dans un moteur de recherche, et/ou dans les tutoriels dédiés.
